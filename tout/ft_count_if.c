int		ft_count_if(char **tab, int (*f)(char*))
{
	int	x;
	int y;

	y = 0;
	x = 0;
	while (tab[x] != '\0')
	{
		if (f(tab[x]) == 1)
			y++;
		x++;
	}
	return (y);
}
