char	*ft_strcapitalize(char *str)
{
	int x;

	x = 0;
	while (str[x] != '\0')
	{
		if (str[x] >= 'a' && str[x] <= 'z')
		{
			str[x] = str[x] - 'a' + 'A';
			while (str[x + 1] >= 'a' && str[x + 1] <= 'z')
				x++;
		}
		else
		{
			if (str[x] >= '0' && str[x] <= '9')
				if (str[x + 1] >= 'a' && str[x + 1] <= 'z')
					while (str[x + 1] >= 'a' && str[x + 1] <= 'z')
						x++;
		}
		x++;
	}
	return (str);
}
