int		ft_srt_is_alpha(char *str)
{
	int x;

	x = 0;
	while (str[x] != '\0')
	{
		if (!(str[x] >= 'a' && str[x] <= 'z'))
			if (!(str[x] >= 'A' && str[x] <= 'Z'))
				if (!(str[x] >= '0' && str[x] <= '9'))
					return (0);
		x++;
	}
	return (1);
}
