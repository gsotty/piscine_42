int		ft_str_is_printable(char *str)
{
	int x;

	x = 0;
	while (str[x] != '\0')
	{
		if (!(str[x] >= ' ' && str[x] <= '~'))
		{
			return (0);
		}
		x++;
	}
	return (1);
}
