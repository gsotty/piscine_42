#include <unistd.h>

int		main(int argc, char **argv)
{
	int		x;
	int		y;
	char	*str;

	str = "president";
	x = 0;
	y = 0;
	if (argc != 1)
	{
		while (argv[x] != '\0')
		{
			while (argv[x][y] != '\0')
			{
				if (argv[x][y] == str[y])
				{
					write(1, "Alert!!!", 8);
					y++;
				}
				y++;
			}
			y = 0;
			x++;
		}
	}
}
