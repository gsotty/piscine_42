#include "header.h"

int		ft_largeur(char **tab)
{
	int		nbr;
	int		x;
	char	new[30];
	int		i;

	x = 0;
	i = 0;
	nbr = 0;
	while (tab[0][x] != ' ')
	{
		x++;
	}
	while (tab[0][x] != '\0')
	{
		new[i] = tab[0][x];
		x++;
		i++;
	}
	nbr = ft_atoi(new);
	return (nbr);
}
