int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int x;

	x = 0;
	while (x < length)
	{
		if (f(tab[x], tab[x + 1]) > 0)
			return (0);
		x++;
	}
	return (1);
}
